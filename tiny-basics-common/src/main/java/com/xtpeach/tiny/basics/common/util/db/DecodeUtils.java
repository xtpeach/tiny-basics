package com.xtpeach.tiny.basics.common.util.db;

/**
 * @author xtpeach
 * 数据库配置解密工具
 */
public class DecodeUtils {

    /**
     * 解密数据库 - 链接
     *
     * @param originalUrl
     * @return
     */
    public static String decodeUrl(String originalUrl) {
        return originalUrl;
    }

    /**
     * 解密数据库 - 用户名
     *
     * @param originalUsername
     * @return
     */
    public static String decodeUsername(String originalUsername) {
        return originalUsername;
    }

    /**
     * 解密数据库 - 密码
     *
     * @param originalPassword
     * @return
     */
    public static String decodePassword(String originalPassword) {
        return originalPassword;
    }

}
